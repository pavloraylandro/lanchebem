    public void insereDadosTabela() {
        DefaultTableModel modeloTabela = (DefaultTableModel) listaatendente.getModel();
        SimpleDateFormat dataNascimento = new SimpleDateFormat("dd/MM/yyyy");
        SimpleDateFormat dataCadastro = new SimpleDateFormat("dd/MM/yyyy");
        AtendenteDAO dao = new AtendenteDAO();
        modeloTabela.setNumRows(0);
        dao.listeAtendentes().stream().forEach((Atendente atendente) -> {
            modeloTabela.addRow(new Object[]{
                atendente.getId(),
                atendente.getNome(),
                atendente.getCpf(),
                dataNascimento.format(atendente.getDatadenascimento().getTime()),
                atendente.getTipoDeFuncionario(),
                atendente.getSalario(),
                dataCadastro.format(atendente.getDataDeCadastro().getTime()),});
        });
    }

