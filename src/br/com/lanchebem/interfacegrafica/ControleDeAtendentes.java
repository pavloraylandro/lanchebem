    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        Atendente atendente = new Atendente();
        atendente.setNome(nome.getText());
        atendente.setCpf(cpf.getText());
        Calendar dataNascimento = null;
        try {
            Date date = new SimpleDateFormat("dd/MM/yyyy").parse(datadenascimento.getText());
            dataNascimento = Calendar.getInstance();
            dataNascimento.setTime(date);
        } catch (ParseException e) {
            JOptionPane.showMessageDialog(null, "Erro de conversão da data");
        }
        atendente.setDatadenascimento(dataNascimento);
        atendente.setSalario(Double.parseDouble(salario.getText()));
        atendente.setTipoDeFuncionario(tipodeatendente.getText());
        atendente.setDataDeCadastro(Calendar.getInstance());
        AtendenteDAO dao = new AtendenteDAO();
        dao.adicionaAtendente(atendente);
        insereDadosTabela();
    }//GEN-LAST:event_jButton1ActionPerformed

    private void listaatendenteMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_listaatendenteMouseClicked
        // TODO add your handling code here:
        if (listaatendente.getSelectedRow() != -1) {
            id.setText(listaatendente.getValueAt(listaatendente.getSelectedRow(), 0).toString());
            nome.setText(listaatendente.getValueAt(listaatendente.getSelectedRow(), 1).toString());
            cpf.setText(listaatendente.getValueAt(listaatendente.getSelectedRow(), 2).toString());
            datadenascimento.setText(listaatendente.getValueAt(listaatendente.getSelectedRow(), 3).toString());
            tipodeatendente.setText(listaatendente.getValueAt(listaatendente.getSelectedRow(), 4).toString());
            salario.setText(listaatendente.getValueAt(listaatendente.getSelectedRow(), 5).toString());
            datadecadastro.setText(listaatendente.getValueAt(listaatendente.getSelectedRow(), 6).toString());
        }

    }//GEN-LAST:event_listaatendenteMouseClicked

